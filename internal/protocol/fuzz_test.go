package protocol

import "testing"

func FuzzProtocolDecoders(f *testing.F) {
	// Seed corpus with valid and invalid inputs
	// Empty and minimal
	f.Add([]byte{})
	f.Add([]byte{0x6a}) // OP_RETURN only
	f.Add([]byte{0, 1, 2, 3, 4, 5})

	// Malformed magic bytes
	f.Add([]byte{0x00, 0x00, 0x00, 0x00}) // Invalid magic
	f.Add([]byte{0xFF, 0xFF, 0xFF, 0xFF}) // Invalid magic
	f.Add([]byte{0x56, 0x50, 0x4E, 0x00}) // VPN with null terminator

	// Zero-length fields
	f.Add([]byte{0x56, 0x50, 0x4E, 0x01, 0x00})                         // VPN, IPv4, zero-length IP
	f.Add([]byte{0x56, 0x50, 0x4E, 0x01, 0x04})                         // VPN, IPv4, but no IP data
	f.Add([]byte{0x56, 0x50, 0x4E, 0x01, 0x04, 0x00, 0x00, 0x00, 0x00}) // IP with zero port

	// Boundary values
	f.Add([]byte{0x56, 0x50, 0x4E, 0x01, 0x04, 0x7F, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}) // Max IPv4, max port

	// IPv6
	f.Add([]byte{0x56, 0x50, 0x4E, 0x01, 0x06}) // VPN with IPv6 type but no data

	// Integer overflow scenarios
	f.Add([]byte{0x56, 0x50, 0x4E, 0x01, 0x04, 0x01, 0x02, 0x03, 0x04, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}) // Price overflow

	// Extremely long payloads
	f.Add(make([]byte, 200))  // Very long
	f.Add(make([]byte, 1000)) // Extremely long

	// PAY payload variations
	f.Add([]byte{0x50, 0x41, 0x59, 0x01})       // PAY with no pubkey
	f.Add([]byte{0x50, 0x41, 0x59, 0x01, 0x00}) // PAY with null pubkey
	f.Add([]byte{0x50, 0x41, 0x59, 0x01, 0xFF}) // PAY with 0xFF

	// Price update payload
	f.Add([]byte{0x50, 0x52, 0x49, 0x01})       // PRICE magic
	f.Add([]byte{0x50, 0x52, 0x49, 0x01, 0x00}) // PRICE with no data

	// Heartbeat payload
	f.Add([]byte{0x48, 0x42, 0x45, 0x01})       // HEARTBEAT magic
	f.Add([]byte{0x48, 0x42, 0x45, 0x01, 0x00}) // HEARTBEAT with no data

	f.Fuzz(func(t *testing.T, data []byte) {
		// Should not panic on any input
		_, _ = DecodePayload(data)
		_, _ = DecodePayloadV2(data)
		_, _ = DecodePayloadV3(data)
		_, _ = DecodePaymentPayload(data)
		_, _ = DecodePriceUpdatePayload(data)
		_, _ = DecodeHeartbeatPayload(data)
		_, _ = ExtractScriptPayload(data)
	})
}
